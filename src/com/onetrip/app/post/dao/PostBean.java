package com.onetrip.app.post.dao;

public class PostBean {

	private int post_num;
	private String post_user_id;
	private int post_city_num;

	public PostBean() {;}

	public int getPost_num() {
		return post_num;
	}

	public void setPost_num(int post_num) {
		this.post_num = post_num;
	}

	public String getPost_user_id() {
		return post_user_id;
	}

	public void setPost_user_id(String post_user_id) {
		this.post_user_id = post_user_id;
	}

	public int getPost_city_num() {
		return post_city_num;
	}

	public void setPost_city_num(int post_city_num) {
		this.post_city_num = post_city_num;
	}
	
	
	
}
