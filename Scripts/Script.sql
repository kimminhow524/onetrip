	CONSTRAINT USER_HP_CK CHECK(LENGTH(USER_HP) = 11 AND REGEXP_LIKE(USER_HP,'^[0-9]*$')), 
	CONSTRAINT USER_EMAIL_CK CHECK(USER_EMAIL LIKE '%@%'),
	CONSTRAINT USER_LOGIN_TYPE_CK CHECK(USER_LOGIN_TYPE IN(0, 1))
);

